import cloudinary from "../lib/cloudinary.js";
import { generateToken } from "../lib/utils.js";
import User from "../models/user.model.js";
import bcrypt from "bcryptjs";

export const signup = async (req, res) => { // use async so can use await
    const {fullName, email, password} = req.body;
    try {
        
        // null check
        if (!fullName || !email || !password) {
            return res.status(400).json({messsage: "All fields are required"});
        }

        // password length check
        if (password.length < 6) {
            return res.status(400).json({message: "Password must be at least 6 characters"}); //status 400 for error message
        }

        const user = await User.findOne({email}); // check if exists in database
        if (user) return res.status(400).json({message: "Email already exists"});

        // hash password
        const salt = await bcrypt.genSalt(10); // any number
        const hashedPassword = await bcrypt.hash(password, salt);

        const newUser = new User({ // create a new user in databse
            fullName: fullName,
            email: email,
            password: hashedPassword
        })

        if (newUser) { // successfully created a new user
            // generate JWT token
            generateToken(newUser._id, res); // _id is auto generated by MongoDB, res as input so that cookie can be sent via res.cookie(...)
            await newUser.save(); // save user to database

            res.status(201).json({
                _id: newUser._id,
                fullName: newUser.fullName,
                email: newUser.email,
                profilePic: newUser.profilePic,
            });

        } else {
            res.status(400).json({message: "Invalid user data"});
        }

    } catch (error) {
        console.log("Error in signup controller", error.message);
        res.status(500).json({message: "Internal server error"});
    }
};

export const login = async (req, res) => {
    const {email, password} = req.body;
    try {
        const user = await User.findOne({email}); // find in database
        if (!user) {
            return res.status(400).json({message: "Invalid credentials"});
        }

        const isPasswordCorrect = await bcrypt.compare(password, user.password); // check if password is correct (compare with hashed password)
        if (!isPasswordCorrect) {
            return res.status(400).json({message: "Invalid credentials"});
        }

        generateToken(user._id, res) // generate JWT token
        res.status(200).json({
            _id: user._id,
            fullName: user.fullName,
            email: user.email,
            profilePic: user.profilePic,
        })
    } catch (error) {
        console.log("Error in login controller", error.message);
        res.status(500).json({message: "Internal Server Error"});
    }
};

export const logout = (req, res) => {
    try {
        res.cookie("jwt", "", {maxAge: 0}); // clear cookie and expire immediately
        res.status(200).json({message: "Logged out successfully"});
    } catch (error) {
        console.log("Error in logout controller", error.message);
        res.status(500).json({message: "Internal Server Error"});
    }
};

export const updateProfile = async (req, res) => {
    // use of cloudinary.com to store images
    try {
        const {profilePic} = req.body;
        const userId = req.user._id;
        if (!profilePic) {
            return res.status(400).json({message: "Profile picture is required"});
        }

        const uploadResponse = await cloudinary.uploader.upload(profilePic);
        const updatedUser = await User.findByIdAndUpdate(userId, {profilePic: uploadResponse.secure_url}, {new:true}); // update databse profilePic to new URL and return the new user (by default is old version)

        res.status(200).json(updatedUser);
    } catch (error) {
        console.log("Error in update profile: ", error);
        res.status(500).json({message: "Internal server error"});
    }
}

